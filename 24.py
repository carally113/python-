print("ведите первое число")
A = int(input())
print("ведите второе число")
B = int(input())
C = A + B
print("плюс", str(A) + "+" + str(B) + "=" + str(C))
S = A - B
print("минус", str(A) + "-" + str(B) + "=" + str(S))
V = A // B
print("деление", str(A) + "//" + str(B) + "=" + str(V))
Z = A * B
print("умножение", str(A) + "*" + str(B) + "=" + str(Z))